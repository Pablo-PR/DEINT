
import java.util.ArrayList;

/**
 *
 * @author pablo
 */
public class NumerosComplejos {
    private ArrayList<Float> parteReal;
    private ArrayList<Float> parteImaginaria;
    
    public NumerosComplejos(float x1, float y1, float x2, float y2){
        this.parteReal.add(x1);
        this.parteReal.add(y1);
        this.parteImaginaria.add(x2);
        this.parteImaginaria.add(y2);
    }
    
    
}
