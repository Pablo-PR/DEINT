    public tableroAjedrez() {
        initComponents();
        
        this.setLayout(new GridLayout(8, 8));
        
        for(int i = 0; i < 64; i++){
            boton[i] = new JButton(Integer.toString(i));
            
            //Controlamos las filas cuya primera casilla es negra
            if(i < 8 || i > 15 && i < 24 || i > 31 && i < 40 || i > 47 && i < 56){
                //En estas filas las casillas pares son de color negro y las impares blancas
                if(i % 2 == 0){
                    boton[i].setBackground(Color.black);
                    boton[i].setForeground(Color.white);
                }else{
                    boton[i].setBackground(Color.white);
                    boton[i].setForeground(Color.black);
                }
            }
            //El resto de filas se colorean de manera inversa
            else{
                if(i % 2 == 0){
                    boton[i].setBackground(Color.white);
                    boton[i].setForeground(Color.black);
                }else{
                    boton[i].setBackground(Color.black);
                    boton[i].setForeground(Color.white);
                }
            }
        }
        
        //AÃ±adimos los botones al layout
        for(int j = 0; j < 64; j++){
            this.add(boton[j]);
        }
        
        this.setTitle("Tablero ajedrez");
        this.setVisible(true);
        this.pack();
    }

