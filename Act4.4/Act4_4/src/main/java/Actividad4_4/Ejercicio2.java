    private void botonAñadirEquipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAñadirEquipoActionPerformed
        // TODO add your handling code here:
        if (tipoSorteo != 0 && !campoNombre.getText().equals("") && !campoPais.getText().equals("")){
            listaEquipos.add(new Equipo(campoNombre.getText(), campoPais.getText()));
            
            if (tipoSorteo == 1 && listaEquipos.size() == 8){
                botonAñadirEquipo.setEnabled(false);
            }
            else if (tipoSorteo == 2 && listaEquipos.size() == 4){
                botonAñadirEquipo.setEnabled(false);
            }
        }
        else if (!campoNombre.getText().equals("") && !campoPais.getText().equals("") && grupoComboBox.getSelectedIndex()!= -1){
            if (posicion1.isSelected()){
                listaEquipos.add(new Equipo(campoNombre.getText(), campoPais.getText(), grupoComboBox.getSelectedItem().toString(), 1));
            }
            else if (posicion2.isSelected()){
                listaEquipos.add(new Equipo(campoNombre.getText(), campoPais.getText(), grupoComboBox.getSelectedItem().toString(), 2));
            }
            
            if (listaEquipos.size() == 16){
                botonAñadirEquipo.setEnabled(false);
            }
        }
        campoNombre.setText("");
        campoPais.setText("");
    }//GEN-LAST:event_botonAñadirEquipoActionPerformed

    private void botonTerminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonTerminarActionPerformed
        // TODO add your handling code here:
        int equiposRestantes = 0;
        String mensaje;
        
        if (tipoSorteo == 0 && listaEquipos.size() != 16){
            equiposRestantes = 16 - listaEquipos.size();
            mensaje = "Te faltan por añadir " + equiposRestantes + " equipos.";
            JOptionPane.showMessageDialog(ventanaError, mensaje);
        }
        else if (tipoSorteo == 1 && listaEquipos.size() != 8){
            equiposRestantes = 8 - listaEquipos.size();
            mensaje = "Te faltan por añadir " + equiposRestantes + " equipos.";
            JOptionPane.showMessageDialog(ventanaError, mensaje);
        }
        else if (tipoSorteo == 2 && listaEquipos.size() != 4){
            equiposRestantes = 4 - listaEquipos.size();
            mensaje = "Te faltan por añadir " + equiposRestantes + " equipos.";
            JOptionPane.showMessageDialog(ventanaError, mensaje);
        }
        else{
            ventanaRegistrar.setVisible(false);
            textAreaListaEquipos.append("EQUIPOS" + "\n");
            
            for (int i=0; i < listaEquipos.size(); i++){
                textAreaListaEquipos.append(i+1 + "." + listaEquipos.get(i).getNombre() + "\n");
            }
            
            botonRegistrar.setEnabled(false);
            botonTipoSorteo.setEnabled(false);
            botonEmparejar.setEnabled(true);
        }
    }//GEN-LAST:event_botonTerminarActionPerformed

