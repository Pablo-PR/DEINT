    private void botonEmparejarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEmparejarActionPerformed
        // TODO add your handling code here:
        ventanaEmparejar.setVisible(true);
        int numEmparejamientos = listaEquipos.size()/2;
        Equipo equipo1 = null;
        Equipo equipo2 = null;
        int numRandom = 0;
        boolean unicoPais = true;
        
        for (int x=0; x < listaEquipos.size() && unicoPais; x++){
            if (listaEquipos.get(x).getPais() != listaEquipos.get(x+1).getPais()){
                unicoPais = false;
            }
        }
        
        for (int i=0; i < numEmparejamientos; i++){
            numRandom = (int) (Math.random()*listaEquipos.size());
            equipo1 = listaEquipos.get(numRandom);
            listaEquipos.remove(numRandom);

            if (tipoSorteo == 0){
                do{
                    numRandom = (int) (Math.random()*listaEquipos.size());
                    equipo2 = listaEquipos.get(numRandom);
                } while (equipo1.getGrupo().equals(equipo2.getGrupo()) || equipo1.getPosicion() == equipo2.getPosicion() || equipo1.getPais().equals(equipo2.getPais()));
            }
            else{
                if (unicoPais){
                    numRandom = (int) (Math.random()*listaEquipos.size());
                    equipo2 = listaEquipos.get(numRandom);
                }
                else{
                    do{
                        numRandom = (int) (Math.random()*listaEquipos.size());
                        equipo2 = listaEquipos.get(numRandom);
                    } while (equipo1.getPais().equals(equipo2.getPais()));
                }
                
            }
            listaEquipos.remove(numRandom);

            textAreaEmparejamientos.append(i+1 + ". " + equipo1.getNombre() + " - "  + equipo2.getNombre() + "\n");
        }
        
    }//GEN-LAST:event_botonEmparejarActionPerformed

